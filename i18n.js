function flattenTranslationSection(section, output = {}) {
  Object.entries(section).forEach(([key, value]) => {
    if (value && typeof value === "object" && !Array.isArray(value)) {
      flattenTranslationSection(value, output);
      return;
    }

    output[key] = value;
  });

  return output;
}

